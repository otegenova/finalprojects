            <div id="Preparation">Preparation</div>
              <div id="prep">
<p>Dough</p>

    <p>Pulse pistachios in a food processor until coarsely ground. Add flour, kosher salt, and sugar and pulse until combined. Add butter and pulse until a coarse meal forms. With motor running, slowly add ½ cup ice water and process until dough comes together. Transfer dough to a work surface, form into a ball, and flatten to a disc. Wrap dough in parchment and chill 30 minutes.</p>

<p>Filling and Assembly</p>

    <p>Preheat oven to 400º. Heat oil in a small skillet over medium-low; cook leek, stirring occasionally and being careful not to brown, until just softened, about 5 minutes.</p>
    <p>Meanwhile, mix cream, goat cheese, and garlic in a medium bowl until smooth. Season with kosher salt and pepper.</p>
    <p>Line a rimmed baking sheet with parchment. Transfer dough to a work surface and divide into 4 pieces. Working with one piece at a time, roll out to an 8" round. Using an offset spatula, spread one-quarter of the goat cheese mixture on dough, leaving a 1" border. Top with 1 Tbsp. dill and a thin layer of potato slices. Drizzle with oil and season with kosher salt. Top with one-quarter of the leeks. Fold edges over, overlapping slightly. Carefully transfer galette to prepared sheet. Repeat with remaining dough and filling. Brush crusts with egg wash.</p>
    <p>Bake galettes until crusts are golden brown and potatoes are soft, 30–40 minutes.</p>
    <p>Drizzle with honey and top with dill. Sprinkle peppercorns and sea salt over, if using. Serve warm.</p>

