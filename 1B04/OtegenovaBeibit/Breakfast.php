            <div id="Servings"><p>Servings: Makes four 6" galettes</p>
<p>Dough</p>

    <p>½ cup shelled pistachios</p>
    <p>2½ cups all-purpose flour</p>
    <p>1 teaspoon kosher salt</p>
    <p>1 teaspoon sugar</p>
    <p>1 cup (2 sticks) chilled unsalted butter, cut into pieces</p>

<p>Filling and Assembly</p>

    <p>1 tablespoon olive oil, plus more for drizzling</p>
    <p>1 large leek, halved lengthwise, white and pale-green parts only, finely chopped<p/>
    <p>¼ cup plus 1 tablespoon heavy cream</p>
    <p>4 ounces goat cheese</p>
    <p>2 garlic cloves, finely chopped
    Kosher salt, freshly ground pepper</p>
    <p>4 tablespoons coarsely chopped dill, plus more for serving</p>
    <p>2 large Yukon Gold potatoes, thinly sliced on a mandoline</p>
    <p>1 large egg, beaten to blend</p>
    <p>Honey (for drizzling)</p>
    <p>Crushed pink peppercorns and flaky sea salt (optional; for serving)</p>

