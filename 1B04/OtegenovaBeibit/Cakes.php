            <div id="Preparation">Preparation</div>
              <div id="prep">
<p>If necessary, preheat oven to 300º, spread bread in a single layer on a baking sheet, and bake to dry out bread, stirring twice, 15–20 minutes; do not let bread toast. This step ensures that the bread can absorb the custard rather than just going gummy when combined with the liquid. If your bread is a day or two old, you needn’t bother.</p>

    <p>Lightly butter a 2-quart baking dish and arrange bread in dish, making sure there are no gaps. Whisk egg yolks in a large bowl, then whisk in granulated sugar; beat about 1 minute to lighten mixture. Whisk in heavy cream, milk, and vanilla extract.</p>

<p>Gently pour custard over bread. Place a heavy skillet or baking dish slightly smaller than the one you are using directly on top of bread to weigh down and refrigerate overnight. This allows the bread to fully absorb the custard—if you only have a few hours, that will suffice.</p>

    <p>Preheat oven to 350º. Remove weight from pudding. Place baking dish in a large roasting pan. Add enough hot water to come halfway up sides of baking dish and carefully transfer to oven. Bake until custard is set around the edges and slightly soft in the center, 40–50 minutes. Remove baking dish from water bath and let cool slightly.</p>
    <p>Combine powdered sugar and cocoa, if using, in a small bowl. Transfer to a sieve and dust pudding just before serving. Serve warm or at room temperature with whipped cream, if using.</p>

