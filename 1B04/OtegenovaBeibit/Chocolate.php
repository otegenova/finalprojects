              <div id="prep">
<p>Cake</p>

    <p>Preheat oven to 350˚. Butter pans and line bottoms with parchment. Butter parchment, then dust with flour, tapping out excess. </p>
    <p>Heat chocolate in a medium heatproof bowl set over a medium saucepan of barely simmering water (do not let water touch bowl), stirring, until chocolate is melted. Let cool, then whisk in buttermilk, vanilla, and almond extract.</p>
    <p>Meanwhile, whisk flour, almond meal, cocoa powder, baking powder, baking soda, and salt in a large bowl until no lumps remain.</p>
    <p>Using an electric mixer on medium-high speed, beat butter and brown sugar in another large bowl until light and fluffy, 3–4 minutes. Add eggs, one at a time, beating to blend between additions and occasionally scraping down sides and bottom of bowl. Continue beating until mixture is almost doubled in volume and very light, airy, and pale yellow, 5–6 minutes—it’s important that no lumps remain.</p>
    <p>Reduce mixer speed to low and add dry ingredients in 3 additions, alternating with buttermilk mixture in 2 additions, beginning and ending with dry ingredients.</p>
    <p>Divide batter evenly between prepared pans; smooth tops. Bake cake until top is firm to the touch and a tester inserted into the center comes out clean, 35–45 minutes. Transfer pans to a wire rack and let cake cool 10 minutes before turning out onto rack. Let cool completely.</p>

<p>frosting and Assembly</p>

    <p>Heat chocolate in a medium heatproof bowl set over a medium saucepan of barely simmering water (do not let water touch bowl), stirring, until chocolate is melted. Let cool, then whisk in vanilla and almond extract.</p>
    <p>Using an electric mixer on medium-high speed, beat powdered sugar, butter, and salt in a medium bowl until light and fluffy, 2–3 minutes. Scrape down sides of bowl, then gradually beat in chocolate mixture until frosting is smooth and shiny and no lumps remain, about 2 minutes. Cover and chill until slightly firmed up, about 30 minutes.</p>
    <p>Place 1 cake layer on a cake stand or large plate. Spread ⅔ cup frosting on cake and smooth to edges with an offset spatula or the back of a spoon. Place second cake layer on top. Spread remaining frosting over top and sides of cake.</p>
