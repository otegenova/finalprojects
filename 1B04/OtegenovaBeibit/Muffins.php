            <div id="Servings"><p>Servings:Makes 12 muffins</p>
<p>Nonstick vegetable oil spray</p>

    <p> 1½ cups all-purpose flour </p>
    <p> 1 cup whole wheat flour </p>
    <p> 1 cup granulated sugar</p>
    <p> 2½ teaspoons baking powder </p>
    <p> 2 teaspoons kosher salt </p>

<p> ½ teaspoon freshly grated nutmeg </p>

    <p> 2 cups fresh blueberries, divided </p>
    <p> 2 large eggs plus 2 large egg yolks, beaten to blend <p/>
    <p> 1½ cups plain whole-milk yogurt </p>
    <p> ½ cup (1 stick) unsalted butter, melted </p>
    <p> 1 teaspoon finely grated lemon zest </p>
    <p> 2 teaspoons fresh lemon juice </p>
    <p> 2 teaspoons vanilla extract </p>
    <p> 2 tablespoons raw sugar </p>

</div>
