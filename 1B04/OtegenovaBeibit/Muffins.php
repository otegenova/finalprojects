            <div id="Preparation">Preparation</div>
              <div id="prep">
<p>Preheat oven to 425°. Lightly coat cups of a standard 12-cup muffin pan with nonstick spray. Whisk all-purpose flour, whole wheat flour, granulated sugar, baking powder, salt, and nutmeg in a large bowl to combine. Add 1½ cups blueberries and gently stir until evenly distributed</p>

    <p>Whisk eggs, yogurt, butter, lemon zest, lemon juice, and vanilla in a medium bowl until combined and creamy, about 30 seconds. Gently fold egg mixture into dry ingredients just to combine (be careful not to overmix).</p>

<p>Divide batter evenly among muffin cups. Top with remaining ½ cup blueberries, gently pressing to adhere (you should have 4–5 berries per muffin). Sprinkle with raw sugar.</p>

    <p>Bake muffins, rotating pan halfway through, until a tester inserted into the center comes out clean, 25–30 minutes. Transfer pan to a wire rack and let muffins cool in pan 5 minutes. Turn out onto rack and let cool completely.</p>

